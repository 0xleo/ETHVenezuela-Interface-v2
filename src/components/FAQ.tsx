import { useState } from 'react';
import './FAQ.css'

export const FAQ = () => {

  const [state1, setState1] = useState(0);
  const [state2, setState2] = useState(0);
  const [state3, setState3] = useState(0);
  const [state4, setState4] = useState(0);

  const handleStateChange = (stateNumber: number) => {

    setState1(0);
    setState2(0);
    setState3(0);
    setState4(0);

    switch (stateNumber) {
      case 1:
        setState1(state1 === 600 ? 0 : 600);
        break;
      case 2:
        setState2(state2 === 600 ? 0 : 600);
        break;
      case 3:
        setState3(state3 === 600 ? 0 : 600);
        break;
      case 4:
        setState4(state4 === 600 ? 0 : 600);
        break;
      default:
        break;
    }
};


return (
  <>

    <div className="container-questions activo" >
      <div className="container-question" onClick={() => handleStateChange(1)}>
        <p className="question">Question1</p>
        <p className="answer" style={{ maxHeight: state1, opacity: state1 }}>--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------</p>
      </div>

      <div className="container-question" onClick={() => handleStateChange(2)}>
        <p className="question">Question2</p>
        <p className="answer" style={{ maxHeight: state2, opacity: state2 }}>--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------</p>
      </div>

      <div className="container-question" onClick={() => handleStateChange(3)}>
        <p className="question">Question3</p>
        <p className="answer" style={{ maxHeight: state3, opacity: state3 }}>--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------</p>
      </div>

      <div className="container-question" onClick={() => handleStateChange(4)}>
        <p className="question">Question4</p>
        <p className="answer" style={{ maxHeight: state4, opacity: state4 }}>--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------</p>
      </div>
    </div>

    </>

)
}

